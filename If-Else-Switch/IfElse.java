public class IfElse {
    public static void main(String[] args) {
        int age = 18;
        if(age >= 75){
            System.out.println("You are an adult");
        } else if (age >= 18) {
            System.out.println("Ok Boomer!");
        }
        else {
            System.out.println("You are a minor");
        }
    }
}

